package operators;

public class RelationalOperators {
     public static void main(String[] args) {
        int a = 20;
        int b = 5;

        System.out.println(a == b);//  output false
        System.out.println(a!= b); // true
        System.out.println(a >= b); // true
        System.out.println(a <= b); // false
        System.out.println(a > b ); // true
        System.out.println(a < b); // false


     }
}
